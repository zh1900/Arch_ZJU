//本实验中内存读写不需要是4的倍数，因为IP核生成的内存块已经固定宽度32位，读的时候只需要指定是第几条
LW $at, 0($zero) //本程序来自于书上，$at=1
LW $v0, 2($zero) //$v0=0,从第3行内存中读取初始值
LW $a0, 1($zero) //$a0=101，读取求和终点
ADD $v1,$at,$v0 //$v1=1
ADD $v0, $v1, $v0  // $v0 is sum
ADD $v1, $v1, $at   // $v1每次加1
BEQ $v1, $a0, 1
J  4  //跳到第4条指令，直接翻译，不除4
SW $v0, 3($zero)  //将结果写到第4行
J  0
